	/**
	 * 字符串反序 // modify by huangyuan at 2012-10-09
	 * 
	 * @param str
	 * @return
	 */
	public static String reverse(String str) {
		StringBuilder resultString = new StringBuilder();
		char[] charArray = str.toCharArray();

		for (int i = charArray.length - 1; i >= 0; i--) {
			resultString.append(charArray[i]);
		}
		return resultString.toString();
	}

	/**
	 * 根据 %1$s 规则替换字符串的 方法
	 * 
	 * @param temp
	 * @param formatArgs
	 * @return
	 */
	public static String getString(String temp, Object... formatArgs) {
		if (locale == null) {
			locale = Locale.getDefault();
		}
		return String.format(locale, temp, formatArgs);
	}

	/**
	 * 处理银行卡号显示 显示前后4位 其他隐藏
	 * 
	 * @param cardNumb
	 * @return
	 */
	public static String getFormatCardNumb(String cardNumb) {
		if (cardNumb == null) {
			return "";
		}
		char[] cs = cardNumb.toCharArray();
		StringBuilder sb = new StringBuilder();
		for (int i = 0; i < cs.length; i++) {
			if (i > 3 && i < cs.length - 4) {
				sb.append("*");
			} else {
				sb.append(cs[i]);
			}
			if (i == 3 || i == cs.length - 5) {
				sb.append(" ");
			}
		}
		return sb.toString();
	}

